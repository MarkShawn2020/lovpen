import esbuild from "esbuild";
import process from "process";
import builtins from "builtin-modules";
import {copy} from "esbuild-plugin-copy";
import {existsSync, mkdirSync, watch} from "fs";
import path from "path";
import {execSync} from "child_process";

const banner =
	`/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`;

const prod = process.env.NODE_ENV === "production";
const obsidianVaultPath = process.env.OBSIDIAN_VAULT_PATH;
const obsidianPluginPath = obsidianVaultPath ? path.join(obsidianVaultPath, '.obsidian', 'plugins', 'obsidian-lovpen') : process.env.OBSIDIAN_PLUGIN_PATH;

// 自动同步到 Obsidian 插件目录的函数
const syncToObsidian = () => {
	if (obsidianPluginPath) {
		// 检查目标目录是否存在，不存在则创建
		if (!existsSync(obsidianPluginPath)) {
			console.log(`📁 Creating Obsidian plugin directory: ${obsidianPluginPath}`);
			try {
				mkdirSync(obsidianPluginPath, {recursive: true});
				console.log(`✅ Directory created successfully`);
			} catch (error) {
				console.error(`❌ Failed to create directory: ${error.message}`);
				process.exit(1);
			}
		}

		try {
			execSync(`rsync -a packages/obsidian/dist/ "${obsidianPluginPath}"`, {
				stdio: 'inherit',
				cwd: path.resolve('../..')
			});
			console.log(`🔄 Synced to Obsidian plugin directory: ${obsidianPluginPath}`);
		} catch (error) {
			console.error('❌ Failed to sync to Obsidian:', error.message);
			process.exit(1);
		}
	}
};

const context = await esbuild.context({
	banner: {
		js: banner,
	},
	entryPoints: ["main.ts"],
	bundle: true,
	external: [
		"obsidian",
		"electron",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		...builtins],
	format: "cjs",
	target: "es2018",
	logLevel: "info",
	sourcemap: prod ? false : "inline",
	treeShaking: true,
	outfile: "dist/main.js",
	plugins: [
		copy({
			// 复制以下资源到 dist/assets 目录
			assets: [
				// 复制插件所需的其他文件到 dist 目录
				{from: ['./manifest.json'], to: ['./manifest.json'], outDir: './dist'},
				{from: ['../assets/**/*'], to: ['./assets/'], outDir: './dist'},
				{from: ['../frontend/dist/**/*'], to: ['./frontend/'], outDir: './dist'},
			],
			verbose: false, // 输出复制操作的日志
		}),
		// 构建完成后自动同步插件
		{
			name: 'obsidian-sync',
			setup(build) {
				build.onEnd((result) => {
					if (result.errors.length === 0) {
						// 构建成功后立即同步
						syncToObsidian();
					}
				});
			},
		},
	],
});


if (prod) {
	await context.rebuild();
	// syncToObsidian(); // 已在 onEnd 钩子中处理
	process.exit(0);
} else {
	await context.watch();
	// 初始构建完成后的同步已在 onEnd 钩子中处理

	let rebuildTimeout;
	const debounceRebuild = () => {
		clearTimeout(rebuildTimeout);
		rebuildTimeout = setTimeout(async () => {
			try {
				console.log('🔄 Frontend assets changed, rebuilding...');
				await context.rebuild();
				console.log('✅ Rebuild completed');
				// syncToObsidian(); // 已在 onEnd 钩子中处理
			} catch (error) {
				console.error('❌ Rebuild failed:', error);
			}
		}, 100); // 100ms防抖
	};

	// 监听变化，触发重新构建
	['../frontend/dist', "../assets"].forEach((p) => {
		const targetPath = path.resolve(p)
		console.log(`🔍 Watching: ${p}`);
		try {
			watch(targetPath, {recursive: true}, (eventType, filename) => {
				if (filename) {
					console.log(`📁 Frontend file changed: ${filename} (${eventType})`);
					debounceRebuild();
				}
			});

		} catch (error) {
			console.warn('⚠️ Could not watch frontend dist directory:', error.message);
			console.log('💡 Make sure to run "pnpm dev" in the frontend package first');
		}
	})
}
